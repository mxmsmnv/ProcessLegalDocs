    protected function loadContextData(): void {
        $context = wire('modules')->getModule('Context', ['noInit' => true]);
        if(!$context) {
            $this->contextData = $this->detectFromLiveAPI();
            return;
        }

        $exportPath = method_exists($context, 'getContextPath') ? $context->getContextPath() : '';
        if(!$exportPath || !is_dir($exportPath)) {
            $this->contextData = $this->detectFromLiveAPI();
            return;
        }

        // Config
        $configData = $this->readJsonFile($exportPath . 'config.json');
        if($configData) {
            $this->contextData['site_name']   = $configData['site_name'] ?? '';
            $this->contextData['pw_version']  = $configData['pw_version'] ?? '';
            $this->contextData['timezone']    = $configData['timezone'] ?? '';
            $this->contextData['exported_at'] = $configData['exported_at'] ?? '';
        }

        // Templates
        $templates = $this->readJsonFile($exportPath . 'templates.json');
        if($templates) {
            $this->contextData = array_merge($this->contextData, $this->analyzeTemplates($templates));
        }

        // Modules
        $modules = $this->readJsonFile($exportPath . 'modules.json');
        if($modules) {
            $this->contextData = array_merge($this->contextData, $this->analyzeModules($modules));
        }

        // Structure
        $structure = $this->readJsonFile($exportPath . 'structure.json');
        if($structure) {
            $this->contextData = array_merge($this->contextData, $this->analyzeStructure($structure));
        }

        // Field definitions
        $fieldDefs = $this->readJsonFile($exportPath . 'metadata/field-definitions.json');
        if($fieldDefs) {
            $this->contextData = array_merge($this->contextData, $this->analyzeFieldDefinitions($fieldDefs));
        }

        // Routes
        $routes = $this->readJsonFile($exportPath . 'metadata/routes.json');
        if($routes) {
            $this->contextData = array_merge($this->contextData, $this->analyzeRoutes($routes));
        }

        // Samples
        $samplesAll = $this->readJsonFile($exportPath . 'samples/_all-samples.json');
        if($samplesAll) {
            $this->contextData = array_merge($this->contextData, $this->analyzeSamples($samplesAll));
        }

        // Supplement with live API
        $liveData = $this->detectFromLiveAPI();
        foreach($liveData as $k => $v) {
            if(empty($this->contextData[$k])) $this->contextData[$k] = $v;
        }
    }

    protected function readJsonFile(string $path): ?array {
        if(!file_exists($path)) return null;
        $data = json_decode(file_get_contents($path), true);
        return is_array($data) ? $data : null;
    }

