    protected function analyzeSamples(array $samplesData): array {
        $result   = ['sample_titles' => [], 'sample_descriptions' => [], 'business_context' => ''];
        $samples  = $samplesData['samples'] ?? $samplesData;
        if(!is_array($samples)) return $result;

        $titles = [];
        $descriptions = [];

        foreach(array_slice($samples, 0, 30) as $sample) {
            if(!is_array($sample)) continue;
            $title = $sample['title'] ?? $sample['name'] ?? '';
            if($title && strlen($title) > 2) $titles[] = $title;
            foreach(['summary', 'description', 'body', 'intro', 'excerpt'] as $field) {
                $val = $sample[$field] ?? '';
                if(is_string($val) && strlen($val) > 20) {
                    $descriptions[] = substr(strip_tags($val), 0, 150);
                    break;
                }
            }
        }

        $result['sample_titles']       = array_unique($titles);
        $result['sample_descriptions'] = array_slice($descriptions, 0, 5);
        if(!empty($titles)) {
            $result['business_context'] = "Sample content from the site: " . implode(', ', array_slice($titles, 0, 10));
        }

        return $result;
    }

