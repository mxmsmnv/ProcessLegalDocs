    protected function getAI() {
        $context = wire('modules')->getModule('Context', ['noInit' => true]);
        if(!$context || !method_exists($context, 'ai')) return null;
        return $context->ai();
    }

    protected function parseFrontmatter(string $content): array {
        if(strpos($content, '---') !== 0) return [];
        $end = strpos($content, '---', 3);
        if($end === false) return [];
        $yaml = substr($content, 4, $end - 4);
        $data = [];
        foreach(explode("\n", $yaml) as $line) {
            if(strpos($line, ':') !== false) {
                list($key, $val) = explode(':', $line, 2);
                $data[trim($key)] = trim(trim($val), '"\'');
            }
        }
        return $data;
    }
