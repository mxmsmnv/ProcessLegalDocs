    protected function buildPrompt(string $jurisdiction, string $documentSlug, string $langCode): string {
        $jurisdictions = ProcessLegalDocsConfig::getJurisdictions();
        $requirements  = ProcessLegalDocsConfig::getJurisdictionRequirements();
        $languages     = ProcessLegalDocsConfig::getLanguages();
        $docTypes      = ProcessLegalDocsConfig::getDocumentTypes();

        $jurName  = $jurisdictions[$jurisdiction]['name'] ?? $jurisdiction;
        $langName = $languages[$langCode] ?? $langCode;
        $docLabel = $docTypes[$documentSlug]['label'] ?? $documentSlug;
        $lawInfo  = $requirements[$jurisdiction] ?? ['law' => $jurisdiction, 'requirements' => []];
        $today    = date('Y-m-d');
        $year     = date('Y');

        $ownerType = $this->config['owner_type'] ?? 'company';
        if($ownerType === 'individual') {
            $ownerName   = trim(($this->config['owner_first_name'] ?? '') . ' ' . ($this->config['owner_last_name'] ?? ''));
            $ownerEntity = 'individual/sole trader';
        } else {
            $ownerName   = $this->config['owner_company_name'] ?? '';
            $ownerEntity = 'company';
        }
        $regNum       = $this->config['owner_reg_number'] ?? '';
        $ownerCountry = $this->config['owner_country'] ?? '';
        $ownerAddress = $this->config['owner_address'] ?? '';
        $ownerEmail   = $this->config['owner_email'] ?? '';
        $websiteUrl   = rtrim($this->config['owner_website'] ?? wire('config')->httpHost, '/');
        $dpoName      = $this->config['dpo_name'] ?? '';
        $dpoEmail     = $this->config['dpo_email'] ?? '';

        // ── System role ───────────────────────────────────────────────────────
        $systemRole  = "You are a senior legal counsel and data protection expert with 20+ years of experience ";
        $systemRole .= "drafting privacy policies, terms of service, and compliance documents for technology companies worldwide.\n\n";
        $systemRole .= "You specialize in:\n";
        $systemRole .= "- GDPR, UK GDPR, CCPA/CPRA, PIPEDA, LGPD, APPI, PIPL, and all major global data protection frameworks\n";
        $systemRole .= "- SaaS, e-commerce, marketplace, and content platforms\n";
        $systemRole .= "- Cross-border data transfer mechanisms (SCCs, adequacy decisions, BCRs)\n";
        $systemRole .= "- Plain-language legal drafting that is both legally precise and user-readable\n\n";
        $systemRole .= "Your documents are used by real businesses and reviewed by lawyers. ";
        $systemRole .= "Every clause must be legally accurate, jurisdiction-specific, and directly relevant to the actual business described.";

        // ── Owner / site context ──────────────────────────────────────────────
        $cd = $this->contextData;

        $siteContext  = "Website: {$websiteUrl}\n";
        $siteContext .= "Owner: {$ownerName} ({$ownerEntity})";
        if($regNum) $siteContext .= ", Reg. No. {$regNum}";
        $siteContext .= "\nRegistered in: {$ownerCountry}\nAddress: {$ownerAddress}\nContact: {$ownerEmail}\n";
        if($dpoName) {
            $siteContext .= "DPO: {$dpoName}";
            if($dpoEmail) $siteContext .= " <{$dpoEmail}>";
            $siteContext .= "\n";
        }

        $businessDetails = [
            'Business description' => $this->config['business_description'] ?? '',
            'Target markets' => $this->config['target_markets'] ?? '',
            'Customer type' => $this->formatCustomerType($this->config['customer_type'] ?? ''),
            'Serves children/minors' => !empty($this->config['serves_children']) ? 'Yes' : '',
            'Minimum age' => $this->config['minimum_age'] ?? '',
            'Data categories collected' => $this->config['data_categories'] ?? '',
            'Sensitive data' => $this->config['sensitive_data_details'] ?? '',
            'Data retention' => $this->config['retention_summary'] ?? '',
            'Processors/sub-processors' => $this->joinPresetAndCustom($this->config['processor_presets'] ?? [], $this->config['processors'] ?? '', 'processors'),
            'Analytics providers' => $this->joinPresetAndCustom($this->config['analytics_presets'] ?? [], $this->config['analytics_providers'] ?? '', 'analytics'),
            'Payment providers' => $this->joinPresetAndCustom($this->config['payment_presets'] ?? [], $this->config['payment_providers'] ?? '', 'payments'),
            'Email/marketing provider' => $this->joinPresetAndCustom($this->config['email_marketing_presets'] ?? [], $this->config['email_marketing_provider'] ?? '', 'email'),
            'Cookie consent tool' => $this->joinPresetAndCustom($this->config['cookie_consent_preset'] ?? '', $this->config['cookie_consent_tool'] ?? '', 'cookies'),
            'Refund policy' => $this->config['refund_policy_summary'] ?? '',
            'Subscription terms' => $this->config['subscription_terms'] ?? '',
        ];

        $hasBusinessDetails = false;
        foreach($businessDetails as $value) {
            if(trim((string) $value) !== '') {
                $hasBusinessDetails = true;
                break;
            }
        }

        if($hasBusinessDetails) {
            $siteContext .= "\n### Business & Compliance Details (provided by site owner)\n";
            foreach($businessDetails as $label => $value) {
                $value = trim((string) $value);
                if($value === '') continue;
                $siteContext .= "{$label}: {$value}\n";
            }
        }

        if(!empty($cd)) {
            $siteContext .= "\n### Site Technical Profile (auto-detected)\n";
            if(!empty($cd['site_name']))   $siteContext .= "Domain: {$cd['site_name']}\n";
            if(!empty($cd['timezone']))    $siteContext .= "Timezone: {$cd['timezone']}\n";
            if(!empty($cd['total_pages'])) $siteContext .= "Total pages: {$cd['total_pages']}\n";
            if(!empty($cd['site_sections'])) {
                $siteContext .= "Main site sections: " . implode(', ', array_slice($cd['site_sections'], 0, 15)) . "\n";
            }
            if(!empty($cd['templates_with_pages'])) {
                $siteContext .= "Active content types: " . implode(', ', array_slice($cd['templates_with_pages'], 0, 12)) . "\n";
            } elseif(!empty($cd['templates'])) {
                $siteContext .= "Content templates: " . implode(', ', array_slice($cd['templates'], 0, 12)) . "\n";
            }

            if(!empty($cd['features'])) {
                $siteContext .= "\n### Features That Affect This Document\n";
                foreach($cd['features'] as $feature) {
                    $siteContext .= "- {$feature}\n";
                }
            }

            $flags = [];
            if(!empty($cd['has_ecommerce']))    $flags[] = "E-COMMERCE: include payment data, order records, transaction history clauses";
            if(!empty($cd['has_auth']))          $flags[] = "USER ACCOUNTS: include account creation, login sessions, account deletion clauses";
            if(!empty($cd['has_forms']))         $flags[] = "FORMS: include form submission data collection and storage clauses";
            if(!empty($cd['has_newsletter']))    $flags[] = "NEWSLETTER: include email subscription, marketing communications, unsubscribe clauses";
            if(!empty($cd['has_multilanguage'])) $flags[] = "MULTI-LANGUAGE: reference language-specific legal obligations";
            if(!empty($cd['has_api']))           $flags[] = "API: include developer/third-party API access data clauses";
            if(!empty($cd['has_cron']))          $flags[] = "SCHEDULED TASKS: include automated background processing clauses";
            if(!empty($cd['has_sessions']))      $flags[] = "DATABASE SESSIONS: include session data storage and retention clauses";
            if(!empty($cd['file_fields']))       $flags[] = "FILE UPLOADS: include user-uploaded file storage, retention, deletion clauses";
            if(!empty($cd['image_fields']))      $flags[] = "IMAGE UPLOADS: include user photo/image handling and privacy clauses";
            if(!empty($cd['payment_fields']))    $flags[] = "PAYMENT DATA: include payment card handling, PCI-DSS considerations";

            if(!empty($flags)) {
                $siteContext .= "\n### MANDATORY CLAUSE TRIGGERS\n";
                foreach($flags as $flag) {
                    $siteContext .= "MUST INCLUDE — {$flag}\n";
                }
            }

            if(!empty($cd['personal_data_fields'])) {
                $siteContext .= "\n### Personal Data Collected (detected from site fields)\n";
                foreach($cd['personal_data_fields'] as $description) {
                    $siteContext .= "- {$description}\n";
                }
            }

            if(!empty($cd['route_features'])) {
                foreach($cd['route_features'] as $rf) {
                    $siteContext .= "- {$rf} (from URL analysis)\n";
                }
            }
            if(!empty($cd['key_urls'])) {
                $siteContext .= "Key URLs detected: " . implode(', ', array_slice($cd['key_urls'], 0, 10)) . "\n";
            }
            if(!empty($cd['business_context'])) {
                $siteContext .= "\n### Real Site Content (sample pages)\n";
                $siteContext .= $cd['business_context'] . "\n";
            }
            if(!empty($cd['sample_descriptions'])) {
                $siteContext .= "Content excerpts:\n";
                foreach(array_slice($cd['sample_descriptions'], 0, 3) as $desc) {
                    $siteContext .= "  — " . $desc . "\n";
                }
            }
            if(!empty($cd['relevant_modules'])) {
                $siteContext .= "Data-relevant modules: " . implode(', ', $cd['relevant_modules']) . "\n";
            }
            if(!empty($cd['exported_at'])) {
                $siteContext .= "Site data snapshot: {$cd['exported_at']}\n";
            }
        }

        // ── Legal framework ───────────────────────────────────────────────────
        $legalFramework  = "Governing law: {$lawInfo['law']}\n";
        $legalFramework .= "Jurisdiction: {$jurName}\n";
        $legalFramework .= "Required legal provisions:\n";
        foreach($lawInfo['requirements'] as $req) {
            $legalFramework .= "  * {$req}\n";
        }

        // ── Document-specific instructions ────────────────────────────────────
        $docInstructions = $this->getDocumentInstructions($documentSlug, $jurisdiction, $jurName, $langName);

        // ── Formatting rules ──────────────────────────────────────────────────
        $formatRules  = "CRITICAL FORMATTING AND QUALITY RULES:\n\n";
        $formatRules .= "1. LANGUAGE: Write the entire document in {$langName}. All headings, body text, legal terms must be in {$langName}.\n\n";
        $formatRules .= "2. NO PLACEHOLDERS: Never use [COMPANY NAME], [INSERT DATE], [YOUR ADDRESS]. Use actual data provided. If info is missing, omit that clause.\n\n";
        $formatRules .= "3. REAL LEGAL CONTENT: Every clause must contain substantive legal text. No vague filler.\n\n";
        $formatRules .= "4. JURISDICTION ACCURACY: Include all legally required sections for {$jurName}. Reference the correct supervisory authority and legal terminology.\n\n";
        $formatRules .= "5. SITE-SPECIFIC: Tailor every section to the actual website described. Only include sections for features the site actually has.\n\n";
        $formatRules .= "6. STRUCTURE: Use ## for main sections, ### for subsections. Number all main sections. Include a table of contents if document exceeds 8 sections.\n\n";
        $formatRules .= "7. DATES: Use \"Last Updated: {$today}\" and \"Effective Date: {$today}\" at the top. Copyright: © {$year} {$ownerName}.\n\n";
        $formatRules .= "8. OUTPUT: Output ONLY the Markdown document. No preamble, no explanation, no code fences. Start directly with the document title.";

        // ── Custom user instructions ──────────────────────────────────────────
        $customInstructions = '';
        $customKey = 'custom_prompt_' . $documentSlug;
        if(!empty($this->config[$customKey])) {
            $customInstructions  = "\n\n## ADDITIONAL INSTRUCTIONS FROM SITE OWNER\n";
            $customInstructions .= "The following instructions MUST be followed:\n";
            $customInstructions .= $this->config[$customKey];
        } elseif(!empty($this->config['custom_prompt_global'])) {
            $customInstructions  = "\n\n## ADDITIONAL INSTRUCTIONS FROM SITE OWNER\n";
            $customInstructions .= $this->config['custom_prompt_global'];
        }

        // ── Assemble ──────────────────────────────────────────────────────────
        $prompt  = $systemRole . "\n\n---\n\n";
        $prompt .= "## YOUR TASK\n\n";
        $prompt .= "Draft a complete, legally binding {$docLabel} for the following business:\n\n";
        $prompt .= $siteContext . "\n";
        $prompt .= "## LEGAL FRAMEWORK\n\n" . $legalFramework . "\n";
        $prompt .= "## DOCUMENT-SPECIFIC REQUIREMENTS\n\n" . $docInstructions . "\n";
        $prompt .= "## FORMATTING AND QUALITY RULES\n\n" . $formatRules;
        if($customInstructions) $prompt .= $customInstructions;

        return $prompt;
    }

