    protected function saveDocument(string $jurisdiction, string $documentSlug, string $langCode, string $markdown): bool {
        $dir  = $this->basePath . '/' . $jurisdiction;
        $path = $this->getDocumentPath($jurisdiction, $documentSlug, $langCode);

        if(!is_dir($dir)) {
            if(!mkdir($dir, 0755, true)) return false;
        }

        $htaccess = $this->basePath . '/.htaccess';
        if(!file_exists($htaccess)) {
            file_put_contents($htaccess, "Deny from all\n");
        }

        $ownerType = $this->config['owner_type'] ?? 'company';
        if($ownerType === 'individual') {
            $ownerName = trim(($this->config['owner_first_name'] ?? '') . ' ' . ($this->config['owner_last_name'] ?? ''));
        } else {
            $ownerName = $this->config['owner_company_name'] ?? '';
        }

        $docTypes  = ProcessLegalDocsConfig::getDocumentTypes();
        $languages = ProcessLegalDocsConfig::getLanguages();
        $jurInfo   = ProcessLegalDocsConfig::getJurisdictions();

        $title    = $docTypes[$documentSlug]['label'] ?? $documentSlug;
        $jurName  = $jurInfo[$jurisdiction]['name'] ?? $jurisdiction;
        $langName = $languages[$langCode] ?? $langCode;
        $website  = $this->config['owner_website'] ?? wire('config')->httpHost;

        $frontmatter  = "---\n";
        $frontmatter .= "title: \"{$title}\"\n";
        $frontmatter .= "jurisdiction: {$jurisdiction}\n";
        $frontmatter .= "jurisdiction_name: \"{$jurName}\"\n";
        $frontmatter .= "language: {$langCode}\n";
        $frontmatter .= "language_name: \"{$langName}\"\n";
        $frontmatter .= "generated: " . date('Y-m-d') . "\n";
        $frontmatter .= "review_status: " . ($this->config['default_review_status'] ?? 'draft') . "\n";
        if(!empty($this->config['reviewer_name'])) {
            $frontmatter .= "reviewer: \"" . $this->escapeYaml((string) $this->config['reviewer_name']) . "\"\n";
        }
        $frontmatter .= "last_reviewed: \n";
        $frontmatter .= "owner: \"{$ownerName}\"\n";
        $frontmatter .= "website: \"{$website}\"\n";
        $frontmatter .= "generator: ProcessLegalDocs for ProcessWire\n";
        $frontmatter .= "---\n\n";

        $disclaimer = self::DISCLAIMER . "\n\n---\n\n";
        $final = $frontmatter . $disclaimer . $markdown;

        if(file_put_contents($path, $final) === false) return false;

        try {
            $this->syncDocumentPage($jurisdiction, $documentSlug, $langCode, $final, $title, $jurName, $langName);
        } catch(\Throwable $e) {
            // Page publishing is optional; keep Markdown generation successful if sync fails.
        }

        return true;
    }

    protected function escapeYaml(string $value): string {
        return str_replace('"', '\"', $value);
    }

    protected function syncDocumentPage(string $jurisdiction, string $documentSlug, string $langCode, string $content, string $title, string $jurName, string $langName): void {
        if(empty($this->config['publish_to_pages'])) return;

        $parentPath = trim((string) ($this->config['publish_parent_path'] ?? ''));
        $templateName = trim((string) ($this->config['publish_template'] ?? ''));
        $bodyField = trim((string) ($this->config['publish_body_field'] ?? 'body'));

        if($parentPath === '' || $templateName === '' || $bodyField === '') return;

        $pages = wire('pages');
        $templates = wire('templates');
        $fields = wire('fields');
        $sanitizer = wire('sanitizer');

        $parent = $pages->get($parentPath);
        $template = $templates->get($templateName);
        $field = $fields->get($bodyField);

        if(!$parent || !$parent->id || !$template || !$field) return;

        $name = $sanitizer->pageName($jurisdiction . '-' . $documentSlug . '-' . $langCode, true);
        $page = $parent->child("name={$name}, include=all");
        if(!$page || !$page->id) {
            $page = new Page();
            $page->template = $template;
            $page->parent = $parent;
            $page->name = $name;
        }

        $page->title = $title . ' — ' . $jurName . ' (' . $langName . ')';
        $page->set($bodyField, $content);

        if(($this->config['publish_status'] ?? 'unpublished') === 'published') {
            $page->removeStatus(Page::statusUnpublished);
        } else {
            $page->addStatus(Page::statusUnpublished);
        }

        $page->of(false);
        $page->save();
    }

