<?php namespace ProcessWire;

/**
 * ProcessLegalDocs — AI Generator
 *
 * Builds prompts, calls AI via Context gateway, saves Markdown files.
 */
class LegalDocsGenerator {

    /** @var array Module config */
    protected $config;

    /** @var string Base path for saving documents */
    protected $basePath;

    /** @var array Cached context data */
    protected $contextData = [];

    const DISCLAIMER = "> ⚠️ **Legal Disclaimer**\n> This document was generated by AI as a template only.\n> It does not constitute legal advice. Laws vary by jurisdiction and change over time.\n> **Consult a qualified lawyer before publishing this document on your website.**\n> *Generated by ProcessLegalDocs for ProcessWire.*";

    public function __construct(array $config, string $basePath) {
        $this->config   = $config;
        $this->basePath = rtrim($basePath, '/');
        $this->loadContextData();
    }

    // =========================================================================
    // Public API
    // =========================================================================

    public function generate(string $jurisdiction, string $documentSlug, string $langCode): array {
        set_time_limit(360); // ensure PHP doesn't kill long AI requests
        $ai = $this->getAI();
        if(!$ai) {
            return ['success' => false, 'error' => 'AI gateway not available. Install and configure the Context module.'];
        }

        $prompt = $this->buildPrompt($jurisdiction, $documentSlug, $langCode);

        $result = $ai->chat([
            'messages'    => [['role' => 'user', 'content' => $prompt]],
            'max_tokens'  => 16000,
            'temperature' => 0.3,
            'timeout'     => 300,
            'caller'      => 'ProcessLegalDocs',
        ]);

        if(isset($result['error'])) {
            return ['success' => false, 'error' => $result['error']];
        }

        $markdown = $result['content'] ?? '';
        if(empty(trim($markdown))) {
            return ['success' => false, 'error' => 'AI returned empty response.'];
        }

        $markdown = preg_replace('/^```(?:markdown)?\s*/i', '', $markdown);
        $markdown = preg_replace('/\s*```$/', '', $markdown);
        $markdown = trim($markdown);

        $saved = $this->saveDocument($jurisdiction, $documentSlug, $langCode, $markdown);

        if(!$saved) {
            return ['success' => false, 'error' => 'Failed to write file. Check directory permissions.'];
        }

        return [
            'success' => true,
            'path'    => $this->getDocumentPath($jurisdiction, $documentSlug, $langCode),
        ];
    }

    public function getDocumentPath(string $jurisdiction, string $documentSlug, string $langCode): string {
        return $this->basePath . '/' . $jurisdiction . '/' . $documentSlug . '.' . $langCode . '.md';
    }

    public function documentExists(string $jurisdiction, string $documentSlug, string $langCode): bool {
        return file_exists($this->getDocumentPath($jurisdiction, $documentSlug, $langCode));
    }

    public function getDocument(string $jurisdiction, string $documentSlug, string $langCode): string {
        $path = $this->getDocumentPath($jurisdiction, $documentSlug, $langCode);
        if(!file_exists($path)) return '';
        return file_get_contents($path);
    }

    public function getDocumentMeta(string $jurisdiction, string $documentSlug, string $langCode): array {
        $content = $this->getDocument($jurisdiction, $documentSlug, $langCode);
        return $this->parseFrontmatter($content);
    }

    public function getGeneratedDocuments(): array {
        $docs = [];
        if(!is_dir($this->basePath)) return $docs;

        foreach(glob($this->basePath . '/*', GLOB_ONLYDIR) as $jurDir) {
            $jurisdiction = basename($jurDir);
            foreach(glob($jurDir . '/*.md') as $file) {
                $filename = basename($file, '.md');
                $parts    = explode('.', $filename);
                $lang     = array_pop($parts);
                $slug     = implode('.', $parts);
                $meta     = $this->getDocumentMeta($jurisdiction, $slug, $lang);
                $docs[]   = [
                    'jurisdiction' => $jurisdiction,
                    'slug'         => $slug,
                    'lang'         => $lang,
                    'path'         => $file,
                    'size'         => filesize($file),
                    'generated'    => $meta['generated'] ?? date('Y-m-d', filemtime($file)),
                    'review_status'=> $meta['review_status'] ?? 'draft',
                    'last_reviewed'=> $meta['last_reviewed'] ?? '',
                    'title'        => $meta['title'] ?? $slug,
                ];
            }
        }

        return $docs;
    }

    public function deleteDocument(string $jurisdiction, string $documentSlug, string $langCode): bool {
        $path = $this->getDocumentPath($jurisdiction, $documentSlug, $langCode);
        if(!file_exists($path)) return false;
        return unlink($path);
    }

    public function exportZip(): string {
        $docs = $this->getGeneratedDocuments();
        if(empty($docs)) return '';

        $zipPath = $this->basePath . '/legal-docs-export-' . date('Y-m-d') . '.zip';

        if(!class_exists('ZipArchive')) return '';

        $zip = new \ZipArchive();
        if($zip->open($zipPath, \ZipArchive::CREATE | \ZipArchive::OVERWRITE) !== true) return '';

        foreach($docs as $doc) {
            if(file_exists($doc['path'])) {
                $zip->addFile($doc['path'], $doc['jurisdiction'] . '/' . basename($doc['path']));
            }
        }

        $zip->close();
        return file_exists($zipPath) ? $zipPath : '';
    }

    public function validateDocument(string $jurisdiction, string $documentSlug, string $langCode): array {
        $content = $this->getDocument($jurisdiction, $documentSlug, $langCode);
        if(empty($content)) return ['missing' => [], 'warnings' => ['Document not found'], 'score' => 0];

        $body = preg_replace('/^---\n.*?\n---\n/s', '', $content);

        $required = $this->getRequiredSections($documentSlug, $jurisdiction);
        $missing  = [];
        $found    = 0;
        $warnings = [];

        foreach($required as $section => $keywords) {
            $detected = false;
            foreach($keywords as $keyword) {
                if(stripos($body, $keyword) !== false) {
                    $detected = true;
                    break;
                }
            }
            if($detected) {
                $found++;
            } else {
                $missing[] = $section;
            }
        }

        $wordCount = str_word_count(strip_tags($body));
        if($wordCount < 300) $warnings[] = "Document seems too short ({$wordCount} words). Consider regenerating.";
        if(preg_match('/\[YOUR [A-Z]+\]|\[INSERT|\[PLACEHOLDER/i', $body)) $warnings[] = "Document contains unfilled placeholders.";
        if(!preg_match('/last updated|effective date/i', $body)) $warnings[] = "No 'Last Updated' date found.";

        $total = count($required);
        $score = $total > 0 ? round(($found / $total) * 100) : 100;

        return [
            'missing'    => $missing,
            'warnings'   => $warnings,
            'score'      => $score,
            'found'      => $found,
            'total'      => $total,
            'word_count' => $wordCount,
        ];
    }

    public function hasAI(): bool {
        $ai = $this->getAI();
        return $ai !== null && $ai->isEnabled();
    }

