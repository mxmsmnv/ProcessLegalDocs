    protected function countPages(array $node, int &$count, array &$sections, int $depth): void {
        foreach($node as $item) {
            if(!is_array($item)) continue;
            if(isset($item['name'])) {
                $count++;
                if($depth === 1 && !empty($item['name'])) $sections[] = $item['name'];
            }
            if(!empty($item['children'])) {
                $this->countPages($item['children'], $count, $sections, $depth + 1);
            }
        }
    }

    protected function analyzeFieldDefinitions(array $fieldDefs): array {
        $typeCounts = [];
